package com.example.ekyc_flutter_sdk;

public class Anchor {
    public float x_center;
    public float y_center;
    public float h;
    public float w;

    public Anchor(float x_center, float y_center, float h, float w) {
        this.x_center = x_center;
        this.y_center = y_center;
        this.h = h;
        this.w = w;
    }
}
